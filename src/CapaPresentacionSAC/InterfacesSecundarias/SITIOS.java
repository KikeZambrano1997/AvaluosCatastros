    private void setFilasSitios(){
        try{

            String sql = "SELECT id, nombre FROM Sitio"; // Where curso= 'Primero'

                PreparedStatement us = con.conexion().prepareStatement(sql);
                ResultSet res = us.executeQuery();

                Object datos[] = new Object[2];
            while(res.next()){
                for(int i=0; i<2; i++){
                    datos[i] = res.getObject(i+1);
                }
                TablaSitios.addRow(datos);
            }

        }
        catch (SQLException e){
            Logger.getLogger(INGRESOCIUDADES.class.getName()).log(Level.SEVERE, null, e);
        }          
    }

    private void LimpiarTabla(){
       for (int i = 0; i < TablaSitios.getRowCount(); i++) {
           TablaSitios.removeRow(i);
           i-=1;
       }
   }

