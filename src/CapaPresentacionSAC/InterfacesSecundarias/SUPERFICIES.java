    private void setFilasSuperficie(){
        try{

            String sql = "SELECT id, superficie, indicador FROM superficie"; 

                PreparedStatement us = con.conexion().prepareStatement(sql);
                ResultSet res = us.executeQuery();

                Object datos[] = new Object[3];
            while(res.next()){
                for(int i=0; i<3; i++){
                    datos[i] = res.getObject(i+1);
                }
                TablaSuperficie.addRow(datos);
            }

        }
        catch (SQLException e){
            Logger.getLogger(EROSION.class.getName()).log(Level.SEVERE, null, e);
        }          
    }

    private void LimpiarTabla(){
       for (int i = 0; i < TablaSuperficie.getRowCount(); i++) {
           TablaSuperficie.removeRow(i);
           i-=1;
       }
   }

