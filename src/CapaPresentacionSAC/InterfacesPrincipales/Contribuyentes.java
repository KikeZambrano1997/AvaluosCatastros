    private void setFilasContribuyentes(){
        try{

            String sql = "SELECT ruc, razon_social, nom_comercial, estado, clase, inicio_act, fec_actualizacion, "
                    + "fec_suspension, reinicio_act, obligado, tipo_cont, num_estab, nom_fantasia, calle, num_calle, "
                    + "interseccion, estado_est, provincia, canton, parroquia, codigo_ciiu, actividad_eco FROM contribuyente";

                PreparedStatement us = con.conexion().prepareStatement(sql);
                ResultSet res = us.executeQuery();

                Object datos[] = new Object[22];
            while(res.next()){
                for(int i=0; i<22; i++){
                    datos[i] = res.getObject(i+1);
                }
                TablaContribuyentes.addRow(datos);
            }

        }
        catch (SQLException e){
            Logger.getLogger(Contribuyentes.class.getName()).log(Level.SEVERE, null, e);
        }          
    }
    
    
    
    private void LimpiarTabla(){
       for (int i = 0; i < TablaContribuyentes.getRowCount(); i++) {
           TablaContribuyentes.removeRow(i);
           i-=1;
       }
   }

