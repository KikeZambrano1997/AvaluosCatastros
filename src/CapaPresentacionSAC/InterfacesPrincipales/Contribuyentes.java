    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ConexionPostgresql a = new ConexionPostgresql();
        LimpiarTabla();
        TablaContribuyentes = new DefaultTableModel(null, getColumnasContribuyentes());

        String ruc_ing = num_ruc.getText();
        ResultSet resultado;
        try{
            
            resultado = a.consultar("SELECT ruc, razon_social, nom_comercial, estado, clase, inicio_act, fec_actualizacion, "
                    + "fec_suspension, reinicio_act, obligado, tipo_cont, num_estab, nom_fantasia, calle, num_calle, "
                    + "interseccion, estado_est, provincia, canton, parroquia, codigo_ciiu, actividad_eco "
                    + "FROM contribuyente WHERE ruc='"+ruc_ing+"'");

                Object datos[] = new Object[22];
            while(resultado.next()){
                for(int i=0; i<22; i++){
                    datos[i] = resultado.getObject(i+1);
                }
                TablaContribuyentes.addRow(datos);
            }

        }
        catch (SQLException e){
            Logger.getLogger(Contribuyentes.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

