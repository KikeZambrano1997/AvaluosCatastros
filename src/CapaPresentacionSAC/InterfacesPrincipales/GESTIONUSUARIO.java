    private void setFilasUsuario(){
        try{

            String sql = "SELECT id, nombre, apellido, usuario, tipo FROM Usuario order by id"; 

                PreparedStatement us = con.conexion().prepareStatement(sql);
                ResultSet res = us.executeQuery();

                Object datos[] = new Object[5];
            while(res.next()){
                for(int i=0; i<5; i++){
                    datos[i] = res.getObject(i+1);
                }
                TablaUsuario.addRow(datos);
            }

        }
        catch (SQLException e){
            Logger.getLogger(GESTIONUSUARIO.class.getName()).log(Level.SEVERE, null, e);
        }          
    }

    private void LimpiarTabla(){
       for (int i = 0; i < TablaUsuario.getRowCount(); i++) {
           TablaUsuario.removeRow(i);
           i-=1;
       }
   }
    
    public void letras (JTextField a){
        a.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if (Character.isDigit(c)){
                    //getToolkit().beep();
                    e.consume();
                }
            }
        });
    }

